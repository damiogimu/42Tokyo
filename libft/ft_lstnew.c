#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	int		i;
	t_list	*new;

	i = 0;
	new = (t_list *)malloc(sizeof(t_list));
	if (new == NULL)
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}
