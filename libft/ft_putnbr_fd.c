#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	num;

	i = 1;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	while (n / i >= 10)
		i *= 10;
	while (i > 0)
	{
		num = (n / i) + '0';
		write(fd, &num, 1);
		n -= (n / i) * i;
		i /= 10;
	}
}
