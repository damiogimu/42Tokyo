#include "libft.h"

int	ft_atoi(const char *s)
{
	int		i;
	int		sign;
	long	num;

	i = 0;
	sign = 1;
	num = 0;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n' || s[i] == '\f'\
			|| s[i] == '\r' || s[i] == '\v')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign *= -1;
		i++;
	}
	while ('0' <= s[i] && s[i] <= '9')
	{
		num *= 10;
		num += s[i] - '0';
		i++;
	}
	return ((int)(num * sign));
}
